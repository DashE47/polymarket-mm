def _wrap(text, font, size, maxw):
    words = text.split(" ")
    lines, cur = [], ""
    for w in words:
        trial = (cur + " " + w).strip()
        if pdfmetrics.stringWidth(trial, font, size) <= maxw or not cur:
            cur = trial
        else:
            lines.append(cur)
            cur = w
    if cur:
        lines.append(cur)
    return "<br/>".join(get_display(ln, base_dir="R") for ln in lines)


def he(text, style, maxw=PAGE_W):
    return Paragraph(_wrap(text, style.fontName, style.fontSize, maxw), style)


